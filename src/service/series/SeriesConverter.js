/**
 * Got methods to convert objects to series.
 * 
 * @class SeriesConverter
 */
class SeriesConverter {

	/**
	 * 
	 * Converts a object to a list.
	 * 
	 * @static
	 * @param {any} object 
	 * @memberof SeriesConverter
	 */
	static convert(object) {
		return object;
	}
}

export default SeriesConverter;